def simple_return(text: str):
    """
    This is simple return
    :param text:
    :return:
    """
    return text


def simple_upper(text: str):
    """
    This is simple_upper
    :param text:
    :return:
    """
    return text.upper()


def simple_power(num: int):
    """
    This is simple_power
    :param num:
    :return:
    """
    return pow(num, 2)


def simple_multi(num: int):
    """
    This is simple_multi
    :param num:
    :return:
    """
    return num*2
